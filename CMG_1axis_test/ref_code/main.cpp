/* USER CODE BEGIN 4 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if (htim == &htim6){
		// every 10 msec (timer6:170Mhz, prescaler:1700, period:1000)

		uhADCxConvertedValue = HAL_ADC_GetValue(&hadc1); //! 0~4095 12bit
		e = bno055.get_eular();//radian

		dphidt = -1 * Kp *  ((e.y * 57.2957795) - compansation_val);

		phi += dphidt * dt;


		//! set PWM based on phi val.
		float pwm_val;
		if (phi < -90.0){
			pwm_val = 20.0;
			phi = -90.0;
		}else if (phi > 90.0){
			pwm_val = 120.0;
			phi = 90.0;
		}else{
			pwm_val = 70.0 + (phi * 0.555556); //! -90~90 into 20~120
		}
		__HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,pwm_val);


	}
}
